/// รวมหลักสูตรทั้งหมด
class CurriculumData {
  static List<EWModule> getModulesForLevel(NCOLevel level) {
    if (level == NCOLevel.junior) {
      return JuniorNCOCurriculum.modules;
    }
    return [...JuniorNCOCurriculum.modules, ...SeniorNCOCurriculum.modules];
  }

  static List<EWModule> get allModules => [
        ...JuniorNCOCurriculum.modules,
        ...SeniorNCOCurriculum.modules,
      ];

  static EWModule? getModuleById(String id) {
    try {
      return allModules.firstWhere((m) => m.id == id);
    } catch (e) {
      return null;
    }
  }
}
